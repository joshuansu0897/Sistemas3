    private void showData() {
        name.setText(cliente.getName());
        description.setText(cliente.getDescription());
    }

    private void save() {
        if (name.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Invalid Name.");
            return;
        }
        cliente.setName(name.getText().trim());

        if (description.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Invalid Description.");
            return;
        }
        cliente.setDescription(description.getText().trim());

        try {
            if (ClienteDB.getInstance().saveCliente(cliente)) {
                JOptionPane.showMessageDialog(this, "Se guardo exitosamente");
                dispose();
            }
        } catch (Exception ex) {
            logger.error("Error al intentar guardar en la base de datos.", ex);
        }
    }
